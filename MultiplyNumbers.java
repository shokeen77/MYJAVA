import java.util.Scanner;

public class MultiplyNumbers {
    public static void main (String[] args){
        Scanner sc = new Scanner(System.in);
        int num = sc.nextInt();
        int num1 = sc.nextInt();
        System.out.println("multiples of two numbers is :- " + (num * num1));

    }
}
